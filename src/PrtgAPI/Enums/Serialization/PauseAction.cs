namespace PrtgAPI
{
    /// <summary>
    /// Specifies states that allow an object to be paused or resumed.
    /// </summary>
    public enum PauseAction
    {
        /// <summary>
        /// Pause the object.
        /// </summary>
        Pause = 0,

        /// <summary>
        /// Resume (unpause) an object.
        /// </summary>
        Resume = 1
    }
}
